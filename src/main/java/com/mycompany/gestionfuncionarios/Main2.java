    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:

        if (txtFuncionarioId.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccionar un funcionario de la lista");
            txtFuncionarioId.requestFocus();
            return;
        }

        if (txtNombreEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite un nombre");
            txtNombreEdit.requestFocus();
            return;
        }

        if (txtApellidoEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite un apellido");
            txtApellidoEdit.requestFocus();
            return;
        }

        if (txtTipoEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite un tpo de documento");
            txtTipoEdit.requestFocus();
            return;
        }

        if (txtNumeroEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite un numero de documento");
            txtNumeroEdit.requestFocus();
            return;
        }

        if (txtDireccionEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite una direccion");
            txtDireccionEdit.requestFocus();
            return;
        }

        if (txtSexoEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el sexo");
            txtSexoEdit.requestFocus();
            return;
        }

        if (txtTelefonoEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el sexo");
            txtTelefonoEdit.requestFocus();
            return;
        }

        if (txtFechaEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite una fecha");
            txtFechaEdit.requestFocus();
            return;
        }

        if (txtNivelEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite un nivel");
            txtNivelEdit.requestFocus();
            return;
        }

        Funcionario funcionario = new Funcionario();
        funcionario.setNombre(txtNombreEdit.getText().trim());
        funcionario.setApellido(txtApellidoEdit.getText().trim());
        funcionario.setTipo_identificacion(txtTipoEdit.getText().trim());
        funcionario.setNum_documento(Integer.parseInt((txtNumeroEdit.getText().trim())));
        funcionario.setDireccion(txtDireccionEdit.getText().trim());
        funcionario.setSexo(txtSexoEdit.getText().trim());
        funcionario.setTel(Integer.parseInt(txtTelefonoEdit.getText().trim()));
        funcionario.setF_nacimiento(txtFechaEdit.getText().trim());
        funcionario.setNivel_educativo(txtNivelEdit.getText().trim());

        int opt = JOptionPane.showConfirmDialog(null, "Desea actualizar el funcionario", "Confirmar Salir", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (opt == 0) {
            try {
                funcionarioController.actualizarFuncionario(Integer.parseInt(txtFuncionarioId.getText()), funcionario);
                txtFuncionarioId.setText("");
                txtNombreEdit.setText("");
                txtApellidoEdit.setText("");
                txtTipoEdit.setText("");
                txtNumeroEdit.setText("");
                txtDireccionEdit.setText("");
                txtSexoEdit.setText("");
                txtTelefonoEdit.setText("");
                txtFechaEdit.setText("");
                txtNivelEdit.setText("");
                listFuncionarios();
                JOptionPane.showMessageDialog(null, "Se ha actualizado el funcionario con exito");
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "No fue posible actualizar el funcionario con exito");
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

