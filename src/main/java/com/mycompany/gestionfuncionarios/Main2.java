    private void listFuncionarios() {
        cbxFuncionarios.removeAllItems();
        Funcionario funcionario1 = new Funcionario();
        funcionario1.setNombre(SELECCIONE);
        funcionario1.setApellido("");
        cbxFuncionarios.addItem(funcionario1);
        tblFuncionarios.removeAll();

        DefaultTableModel defaultTableModel = new DefaultTableModel();
        for (String COLUMN : COLUMNS) {
            defaultTableModel.addColumn(COLUMN);
        }
        tblFuncionarios.setModel(defaultTableModel);

        try {
            List<Funcionario> funcionarios = funcionarios = funcionarioController.obtenerFuncionarios();
            if (funcionarios.isEmpty()) {
                return;
            }
            defaultTableModel.setRowCount(funcionarios.size());
            int row = 0;
            for (Funcionario funcionario : funcionarios) {
                defaultTableModel.setValueAt(funcionario.getId_funcionario(), row, 0);
                defaultTableModel.setValueAt(funcionario.getNombre(), row, 1);
                defaultTableModel.setValueAt(funcionario.getApellido(), row, 2);
                defaultTableModel.setValueAt(funcionario.getTipo_identificacion(), row, 3);
                defaultTableModel.setValueAt(funcionario.getNum_documento(), row, 4);
                defaultTableModel.setValueAt(funcionario.getDireccion(), row, 5);
                defaultTableModel.setValueAt(funcionario.getSexo(), row, 6);
                defaultTableModel.setValueAt(funcionario.getTel(), row, 7);
                defaultTableModel.setValueAt(funcionario.getF_nacimiento(), row, 8);
                defaultTableModel.setValueAt(funcionario.getNivel_educativo(), row, 9);
                row++;

                cbxFuncionarios.addItem(funcionario);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

    private void addListener() {
        cbxFuncionarios.addItemListener(event -> {
            Funcionario selectedFuncionario = (Funcionario) event.getItem();
            if (selectedFuncionario.getNombre().equals(SELECCIONE)) {
                txtFuncionarioId.setText("");
                txtNombreEdit.setText("");
                txtApellidoEdit.setText("");
                txtTipoEdit.setText("");
                txtNumeroEdit.setText("");
                txtDireccionEdit.setText("");
                txtSexoEdit.setText("");
                txtTelefonoEdit.setText("");
                txtFechaEdit.setText("");
                txtNivelEdit.setText("");

            } else {
                txtFuncionarioId.setText(String.valueOf(selectedFuncionario.getId_funcionario()));
                txtNombreEdit.setText(selectedFuncionario.getNombre());
                txtApellidoEdit.setText(selectedFuncionario.getApellido());
                txtTipoEdit.setText(selectedFuncionario.getTipo_identificacion());
                txtNumeroEdit.setText(String.valueOf(selectedFuncionario.getNum_documento()));
                txtDireccionEdit.setText(selectedFuncionario.getDireccion());
                txtSexoEdit.setText(selectedFuncionario.getSexo());
                txtTelefonoEdit.setText(String.valueOf(selectedFuncionario.getTel()));
                txtFechaEdit.setText(String.valueOf(selectedFuncionario.getF_nacimiento()));
                txtNivelEdit.setText(String.valueOf(selectedFuncionario.getNivel_educativo()));
            }
        });
    }

